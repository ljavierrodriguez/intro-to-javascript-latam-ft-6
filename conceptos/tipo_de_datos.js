/* 
Tipos de Datos en JavaScript

Number: Representa tanto enteros como números de punto flotante. Ejemplo: 42, 3.14
String: Cadena de texto. Ejemplo: "Hola, Mundo!"
Boolean: Representa un valor verdadero o falso. Ejemplo: true, false
Undefined: Indica que una variable no ha sido asignada. Ejemplo: let x;

Null: Representa la ausencia intencional de cualquier valor. Ejemplo: let y = null;
Object: Colección de pares clave-valor. Ejemplo: { nombre: "Juan", edad: 30 }
Array: Lista ordenada de valores. Ejemplo: [1, 2, 3, 4, 5], ["manzana", "banana", "cereza"]
Function: Bloque de código diseñado para realizar una tarea específica. Ejemplo: function saludar() { console.log("Hola"); }
*/
